/*******************************************************************************
* 函数名  : USART3_Init_Config
* 描述    : USART3初始化配置
* 输入    : bound：波特率(常用：2400、4800、9600、19200、38400、115200等)
* 输出    : 无
* 返回    : 无 
* 说明    : 无
*******************************************************************************/
void USART3_Init_Config(u32 bound)
{
	GPIO_InitTypeDef  GPIO_InitStructure;     //声明一个结构体变量，用来初始化GPIO
	USART_InitTypeDef USART_InitStructure;    
	NVIC_InitTypeDef  NVIC_InitStructure;

	/*使能USART3外设时钟*/  
	RCC_APB1PeriphClockCmd(RCC_APB1Periph_USART3, ENABLE);	  //使能UART3所在的GPIOB的时钟
	RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOB, ENABLE);	
	/*复位串口3*/
	USART_DeInit(USART3);  

	/*USART3_GPIO初始化设置*/ 
	//configure UART_TX(PB 10) as alternate function push-pull
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_10;			//USART3_TXD(PB10)     
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;		//复用推挽输出
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;	//设置引脚输出最大速率为50MHz
	GPIO_Init(GPIOB, &GPIO_InitStructure);				//调用库函数中的GPIO初始化函数，初始化USART3_TXD(PB10)  

  // congigure UART_RX(PB11) as input floating  
	GPIO_InitStructure.GPIO_Pin = GPIO_Pin_11;				//USART3_RXD(PB.11)
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;	//浮空输入
	GPIO_Init(GPIOB, &GPIO_InitStructure);					//调用库函数中的GPIO初始化函数，初始化USART3_RXD(PB11)


	/*USART3 初始化设置*/
	USART_InitStructure.USART_BaudRate = bound;										//设置波特率
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;						//8位数据格式
	USART_InitStructure.USART_StopBits = USART_StopBits_1;							//1个停止位
	USART_InitStructure.USART_Parity = USART_Parity_No;								//无奇偶校验位
	USART_InitStructure.USART_HardwareFlowControl = USART_HardwareFlowControl_None;	//无硬件数据流控制
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;					//工作模式设置为收发模式
	USART_Init(USART3, &USART_InitStructure);										//初始化串口3

	

	USART_ITConfig(USART3, USART_IT_RXNE, ENABLE);			//使能串口3接收中断

	USART_Cmd(USART3, ENABLE);                    			//使能串口 
	USART_ClearFlag(USART3, USART_FLAG_TC);					//清除发送完成标志
}
